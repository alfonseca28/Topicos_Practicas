    public boolean validar(String sNombre) {
        int iTam = this.cbpelis.getItemCount();
        for (int i = 0; i < iTam; i++) {
            if (cbpelis.getItemAt(i).equals(sNombre)) {
                return true;
            }
        }
        return false;
    }

