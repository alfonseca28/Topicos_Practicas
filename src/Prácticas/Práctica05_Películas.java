    private void botonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActionPerformed
        String sPel = txtPeliculas.getText();
        if (validar(sPel)) {
            JOptionPane.showMessageDialog(this, "La película " + sPel + " ya se introdujo...");
        } else if (sPel.length() != 0)
            cbpelis.addItem(sPel);
    }//GEN-LAST:event_botonActionPerformed

